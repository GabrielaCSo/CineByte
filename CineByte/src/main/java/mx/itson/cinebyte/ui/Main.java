    private void btnReporteGenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReporteGenActionPerformed
        String boletosString = "";
        for (Boleto b : boletosVendidos) {
            boletosString += "nombre: " + b.getNombre()
                    + ", funcion: " + b.getFuncion()
                    + ", precio: " + b.getPrecio()
                    + ", asiento/s: " + b.getAsiento() + "\n";
        }
        JOptionPane.showMessageDialog(this, boletosString, "Reporte de ventas", 1);
    }//GEN-LAST:event_btnReporteGenActionPerformed

