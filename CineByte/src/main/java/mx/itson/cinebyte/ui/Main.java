    private void brnReportePelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnReportePelActionPerformed
        int boletosVenom = 0;
        int boletosGladiador = 0;
        int boletosSonrie = 0;
        double gananciasVenom = 0;
        double gananciasGladiador = 0;
        double gananciasSonrie = 0;

        for (Boleto b : boletosVendidos) {
            switch (b.getFuncion()) {
                case "Venom el último baile":
                    boletosVenom++;
                    gananciasVenom += b.getPrecio();
                    break;
                case "Gladiadores (ReEstreno)":
                    boletosGladiador++;
                    gananciasGladiador += b.getPrecio();
                    break;
                case "Sonrie 2":
                    boletosSonrie++;
                    gananciasSonrie += b.getPrecio();
                    break;
            }
        }
        JOptionPane.showMessageDialog(this, "Venom: \n" 
                +"Cantidad de boletos: "+ boletosVenom+ "\t Ganancias: "+ gananciasVenom +"\n"+
                    "Gladiadores (ReEstreno): \n" 
                +"Cantidad de boletos: "+ boletosGladiador+ "\t Ganancias: "+ gananciasGladiador +"\n"+
                        "Sonrie 2: \n" 
                +"Cantidad de boletos: "+ boletosSonrie+ "\t Ganancias: "+ gananciasSonrie +"\n"
                , "Reporte de ventas por funcion", 1);


    }//GEN-LAST:event_brnReportePelActionPerformed

